class TestCreateMaze:
    """INTENT: returns a dict of {(x,y): [neighbors]} generated by random DFS."""

    def test_returns_dict(self):
        m = create_maze(5, 5)
        assert isinstance(m, dict)
        # All keys are coord tuples
        for k in m.keys():
            assert isinstance(k, tuple)
            assert len(k) == 2
        # All values are lists of coord tuples
        for v in m.values():
            assert isinstance(v, list)
            for n in v:
                assert isinstance(n, tuple)
                assert len(n) == 2

    def test_uses_width_and_height(self):
        m_small = create_maze(3, 3)
        m_big = create_maze(8, 8)
        # bigger maze should have at least as many cells
        assert len(m_big) >= len(m_small)


