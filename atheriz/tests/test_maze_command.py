"""Intent-focused tests for the MazeCommand and its maze-generation helpers.

INTENT: Documents the *behavior* of:
  - MazeCommand.run: builder-only, generates 3 mazes, wires exits between them
  - create_maze: returns a dict {(x,y): [(nx,ny), ...]} of valid neighbors
  - create_map: turns the dict into a {coord: glyph} map + NodeGrid with linked nodes
  - map_to_string: pretty-prints the map list
"""
from __future__ import annotations

from unittest.mock import MagicMock, patch

import pytest

from atheriz import settings
from atheriz.commands.loggedin.maze import (
    MazeCommand,
    create_map,
    create_maze,
    gen_map_and_grid,
    map_to_string,
)
from atheriz.globals.get import get_async_threadpool, get_node_handler
from atheriz.objects.base_obj import Object
from atheriz.utils import Coord


def _make_caller(name="Alice", builder=False):
    c = MagicMock(spec=Object)
    c.name = name
    c.id = 1
    c.privilege_level = settings.Privilege.Builder if builder else settings.Privilege.Player
    c.quelled = False
    c.is_builder = builder
    c.is_superuser = False
    c.location = None
    c.map_enabled = False
    c.msg = MagicMock()
    c.move_to = MagicMock()
    return c


# ---------------------------------------------------------------------------
# MazeCommand
# ---------------------------------------------------------------------------

class TestMazeCommand:
    """INTENT: builder-only; generates 3 mazes (50x20), creates 3 NodeAreas,
    wires down-links between them, schedules an async pathfind on the threadpool,
    and moves the caller to maze1 (0,0,0)."""

    def test_access_requires_builder(self):
        c = _make_caller(builder=False)
        assert MazeCommand().access(c) is False

    def test_access_allowed_for_builder(self):
        c = _make_caller(builder=True)
        assert MazeCommand().access(c) is True

    def test_generates_three_areas_and_announces(self):
        c = _make_caller(builder=True)
        nh = get_node_handler()
        with patch("atheriz.commands.loggedin.maze.get_async_threadpool") as mock_pool, \
             patch("atheriz.commands.loggedin.maze.get_map_handler") as mock_mh, \
             patch("atheriz.commands.loggedin.maze.astar", return_value=(True, [], [])):
            mock_mh.return_value = MagicMock()
            mock_pool.return_value = MagicMock()
            MazeCommand().run(c, None)
        # Caller is told it created the mazes
        msgs = [str(call) for call in c.msg.call_args_list]
        assert any("created 3" in m for m in msgs)
        # Three areas should be registered with the node handler
        assert "maze1" in nh.areas
        assert "maze2" in nh.areas
        assert "maze3" in nh.areas
        # Caller moved to maze1 (0,0,0)
        c.move_to.assert_called_once()
        moved_to = c.move_to.call_args.args[0]
        assert moved_to.coord == Coord("maze1", 0, 0, 0)
        # Map enabled
        assert c.map_enabled is True

    def test_no_node_at_origin_skips_move(self):
        # INTENT: when maze1 has no node at (0,0,0) the moving-to message and move_to are skipped.
        # We test this by mocking nh.get_node(Coord("maze1",0,0,0)) to return None.
        c = _make_caller(builder=True)
        nh = get_node_handler()
        with patch("atheriz.commands.loggedin.maze.get_async_threadpool") as mock_pool, \
             patch("atheriz.commands.loggedin.maze.get_map_handler") as mock_mh, \
             patch("atheriz.commands.loggedin.maze.astar", return_value=(True, [], [])), \
             patch("atheriz.commands.loggedin.maze.get_node_handler", return_value=nh) as mock_get_nh:
            mock_mh.return_value = MagicMock()
            mock_pool.return_value = MagicMock()
            # Make get_node(Coord("maze1",0,0,0)) return None
            nh.get_node = MagicMock(side_effect=lambda coord: None if coord == Coord("maze1", 0, 0, 0) else MagicMock())
            MazeCommand().run(c, None)
        # move_to was NOT called
        c.move_to.assert_not_called()


